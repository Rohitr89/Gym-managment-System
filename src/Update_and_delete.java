    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String id = member_id_tf.getText();
        String name = name_tf.getText();
        String mobile_no = mobile_no_tf.getText();
        String gmail = gmail_tf.getText();
        String father = father_tf.getText();
        String mother = mother_tf.getText();
        String aadhar = aadhar_tf.getText();
        String age = age_tf.getText();
        String amount = amount_tf.getText();
        try 
        {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("update member set name=?,mobile_no = ?,email = ?,father_Name = ?,mother_Name = ?,aadhar_No = ?,age =? ,amount = ? where id = ?");
            ps.setString(1, name);
            ps.setString(2, mobile_no);
            ps.setString(3,gmail);
            ps.setString(4,father);
            ps.setString(5,mother);
            ps.setString(6,aadhar);
            ps.setString(7,age);
            ps.setString(8,amount);
            ps.setString(9, id);
            ps.executeUpdate();
            JOptionPane.showConfirmDialog(null,"Successfully Updated");
            setVisible(false);
            new Update_and_delete().setVisible(true);
        }
        catch(Exception e)
        {
            JOptionPane.showConfirmDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

