    private void search_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_buttonActionPerformed
        // TODO add your handling code here:
        int check_id = 0;
        String id = member_id_tf.getText();
        try 
        {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from member where id ='" + id + "'");
            while(rs.next())
            {
                 check_id = 1;
                 member_id_tf.setEditable(false);
                 name_tf.setText(rs.getString(2));
                 mobile_no_tf.setText(rs.getString(3));
                 gmail_tf.setText(rs.getString(4));
                 gender_tf.setText(rs.getString(5));
                 gender_tf.setEditable(false);
                 father_tf.setText(rs.getString(6));
                 mother_tf.setText(rs.getString(7));
                 batch_timing_tf.setText(rs.getString(8));
                 batch_timing_tf.setEditable(false);
                 aadhar_tf.setText(rs.getString(9));
                 age_tf.setText(rs.getString(10));
                 amount_tf.setText(rs.getString(11));
                 
            }
            if(check_id == 0)
            {
                JOptionPane.showMessageDialog(null,"Member id does'nt exist");
            }
                    
        }
        catch(Exception e)
        {
            
        }
        
    }//GEN-LAST:event_search_buttonActionPerformed

